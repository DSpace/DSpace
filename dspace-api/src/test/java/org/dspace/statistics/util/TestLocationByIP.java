    @Test
    public void testGetPrintCityCountryContinentForBatch() throws IOException, GeoIp2Exception
    {
        loadIpList();
        
        String dbPath = ConfigurationManager.getProperty("usage-statistics", "dbfile");
        if (dbPath != null) {
                File dbFile = new File(dbPath);
                DatabaseReader locationService = new DatabaseReader.Builder(dbFile).build();
                for (String ip : ipList) {
                    InetAddress ipAddress = InetAddress.getByName(ip);
                    CityResponse location = locationService.city(ipAddress);
                    String countryCode = location.getCountry().getIsoCode();
                    String continentCode = location.getContinent().getCode();
                    if(countryCode != null && !countryCode.isEmpty()) {
                        log.info(String.format("[MATCH] IP: %s - City: %s - Country: %s - "
                                + "Continent: %s", ip, location.getCity().getName(), countryCode, continentCode));
                    } else {
                        log.info(String.format("[NOT MATCH] IP: %s", ip));
                    }
                }
        } else {
            throw new FileNotFoundException("No existe la ruta al archivo de geoDB: " + dbPath);
        }
        
    }

